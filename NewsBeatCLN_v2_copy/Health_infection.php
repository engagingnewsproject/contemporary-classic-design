<p class="MainText">About 1 in every 25 patients seeking treatment at hospitals acquired an infection there in 2011, according to a new study published Wednesday in the New England Journal of Medicine.</p><p class="MainText">

Patients acquired some 721,800 infections at hospitals that year, according to the research. Of those infected, about 75,000 died, according to the U.S. Centers for Disease Control and Prevention &ndash; although the study did not investigate how often an infection actually caused or contributed to the patient's death.</p><p class="MainText">

Pneumonia and surgical-site infections were the most common types of infection &ndash; each accounting for about 22% of all infections &ndash; followed by gastrointestinal infections such as Clostridium difficile, urinary tract infections and infections of the bloodstream.</p>
