  <p class="Photocaption">The rate of patient infections acquired at hospitals has decreased, but is still worrisome, experts say.</p>
  <hr>
<p class="MainText">While highlighting the grim reality that too many people become infected when seeking medical treatment in hospitals and other health care facilities, the study also shows progress from past estimates.</p><p class="MainText">

In 2002, there were an estimated 1.7 million health care-associated infections and 155,668 infected-patient deaths. In 98,987 of those deaths, clinicians said the patient's infection actually caused or contributed to death.</p><p class="MainText">

When it comes to reducing infection rates, "there's great data showing that we can actually move the needle significantly," says Dr. Michael Bell, director of the CDC's Division of Healthcare Quality Promotion.</p><p class="MainText">

For example, bloodstream infections from central lines (lines inserted in the chest into a vein) dropped 44% between 2008 and 2012 &ndash; a result, say experts, of requiring hospitals to follow a simple checklist of best practices.</p><p class="MainText">

"But at the same time, we're not doing a perfect job of doing everything we should, every single time," says Bell, "and there's some things which we haven't figured out how best to manage &ndash; yet."</p><p class="MainText">

C. difficile infections, for example, have become more severe in the past decade, and more bacteria are resistant to antibiotics. Antibiotic-resistant infections account for at least 23,000 deaths each year, according to the CDC.</p><p class="MainText">

Still, hospital infection rates vary significantly between states, and even between neighboring hospitals.</p><p class="MainText">

"There are some excellent places that are doing a bang-up job, and I would love to receive my care there," Bell says.</p><p class="MainText">

"Also within every facility type there are places that give me great concern. It can be related to staffing and resources; it could be related to the way things get done."</p><p class="MainText">

Dr. Peter Pronovost, the director of the Armstrong Institute for Patient Safety and Quality at Johns Hopkins, recommends patients research their local hospital's infection rates on the Centers for Medicare and Medicaid Services hospital comparison website ahead of time, if at all possible.</p><p class="MainText">

"These ICU bloodstream infections are often a 'canary in the coal mine' measure," says Pronovost. "In other words, if a hospital gets its act together to focus on safety and gets these down, it's a good marker that they're working collaboratively, even if you're not in the ICU."</p><p class="MainText">

Once inside a facility, Pronovost says, patients can hold clinicians accountable to best practices by asking doctors and nurses to wash their hands when they enter the room and by inquiring about the appropriate time for catheters and other medical devices &ndash; common sources of infection &ndash; to be removed.</p><p class="MainText">

"Even though we've had great success nationally, there still are pockets of hospitals that have rates of infection that are several times the national average. The reality is that oftentimes there's very little that's being done about it," says Pronovost. "There's no accountability for a hospital that has very high infection rates, and my sense is, there absolutely needs to be."</p><p class="MainText">

The study included 183 hospitals and 11,282 patients surveyed between May and September 2011.</p>
