<table bgcolor="#FFFFFF"; style="text-wrap:normal; word-wrap:normal; max-width:730px" cellpadding="10px"><tr><td align="left">

<p class="MainText">The photography world has been ruled in recent years by a fascination with abstraction and experimental processes and techniques. You see this everywhere in the current Association of International Photography Art Dealers show at the Park Avenue Armory. Even Hans P. Kraus Jr., who deals in photographic old masters, is showing &ndash; alongside vintage prints by Charles Marville, now featured in a show at the Metropolitan Museum, and Eug&eacute;ne Atget &ndash; some 19th-century pencil drawings by John Herschel, an important contributor to early photography. The drawings here, of Stonehenge and sights on the European Grand Tour, were made with a camera lucida, an optical device that functions in a manner similar to that of the camera obscura but in full daylight rather than a dark room.</p><p class="MainText">

Photography by 1930s Surrealists like Raoul Ubac is on view at Contemporary Works, while Gitterman is showing photographs from the 1940s by Josef Breitenbach, who taught at Black Mountain College in North Carolina and combined the Photogram technique with chemical manipulation and other darkroom methods.</p>

