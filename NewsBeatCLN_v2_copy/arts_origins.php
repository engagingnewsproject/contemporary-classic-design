<p class="MainText">Photograms, cameraless images made by exposing light-sensitive paper, are everywhere here. Von Lintel has contemporary ones by Farrah Karapetian and Wendy Small, as well as Floris Neus&uuml;ss, who has devoted his career to making them. Joel Soroka has modern black-and-white photograms by the Hungarian-born artist Gy&ouml;rgy Kepes and Ei-Kyu of Japan, as well as a large vintage oil print, believed to be unique, of a still life with scissors, thimble and needle, by Pierre Dubreuil. Yancey Richardson is showing a large color photogram with a lattice pattern, reminiscent of Henry Fox Talbot's early calotypes, by Bryan Graf. Gitterman has a chemigram, a process invented in the 1950s that works something like wax resistance in batik, by the German artist known simply as Chargesheimer.</p><p class="MainText">

Other works highlighting experimental techniques include those of Jessica Eaton at M+B, who uses photographic filters to create compositions that look like Josef Albers paintings. Mishka Henner's "Dutch Landscape" series at Yossi Milo features satellite images in which security sites have been stylishly blotted out with colorful polygons at the initiative of the government of the Netherlands. Robert Koch is showing a rare photosculpture &ndash; gelatin silver prints mounted on a rectangular column &ndash; by Robert Heinecken, a seminal artist in '60s and '70s Conceptual photography who currently has a retrospective at the Museum of Modern Art.</p><p class="MainText">

Other works that extend the medium in interesting directions include Jennifer Williams's small, sculptural collages made with photographs, at Robert Mann; John Wood's collages and assemblages at Bruce Silverstein; and Wynn Bullock's "Color Light Abstractions" from the '60s at Rick Wester. Although there is plenty of straight photography here, including historical photojournalism at Daniel Blau, it's the drive toward process, abstraction and experimentation that makes this fair feel particularly relevant.

</p>

