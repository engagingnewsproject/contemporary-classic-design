  <p class="Photocaption">A study from American University found women who play college sports are 25 percent more likely to have political ambitions than their non-athlete counterparts.</p>
  <hr>
<p class="MainText">For a study, Professor Jennifer Lawless at American University asked 2,100 college students if they would consider a career in politics..</p><p  class="MainText">

"The effect was quite substantial," Lawless says. "Women who played sports and were competitive playing sports were about 25 percent more likely to express an interest in running for office later in life."</p><p  class="MainText">

There's a boost for men, too, but it's not as big. Serious male athletes are about 15 percent more likely to think about getting into politics. Thing is, men across the board are already much more disposed than women to run. So Lawless sees encouraging girls to play sports as one way to start equalizing male and female political ambition."</p><p  class="MainText">

"It's clearly a way that we can generate more interest among women and get them to think about running for office," she says.</p><p  class="MainText">

Now, casually participating in sports isn't enough to have an impact on political ambition. It's the athletes who say they really care about winning who are more likely to consider running. Lawless says there are a couple of skills they pick up playing sports that transfer well to politics.</p><p  class="MainText">

"The first is the ability to compete and the willingness to lose," she says. "In most cases, if you like sports and you're competitive, although you probably prefer to win, you've gained some familiarity with losing, and you know it's not the end of the world."</p><p  class="MainText">

That squares with Gillibrand's experience. She says it was a squash match she lost in college that proved most formative. "I played one tough match I remember at Yale. I was so over my head, I got crushed," she says.</p><p  class="MainText">

She says it was painful at the time, but it also helped prepare her for the contact sport of politics later in life. "I think it takes a level of fear out of something like running for office and putting yourself out there in a competitive contest and letting people choose," she says.</p><p  class="MainText">

Gillibrand is far from the only female athlete serving on Capitol Hill. Republican Sen. Kelly Ayotte from New Hampshire was a competitive skier in college. She and Gillibrand are in their 40s. They are among the youngest members of the U.S. Senate.</p><p  class="MainText">

And in the House, Democrat Rep. Tulsi Gabbard of Hawaii is a 32-year-old surfer who's also participated in a lot of martial arts training.</p><p  class="MainText">

Jessica Grounds, co-founder of the group Running Start, an organization that encourages women to run for office, points out that politicians like Gillibrand and Ayotte came of age after the passage of Title IX in 1972. That's the legislation that mandated girls and women have equal access to playing sports.</p><p  class="MainText">

"We see strong correlations between women who played sports and are now successful CEOs of companies and are not only running for office but successful in their leadership positions," Grounds says.</p><p  class="MainText">

Since Title IX passed, the number of girls and women participating in school sports went from about 300,000 to more than 3 million. That has implications beyond high school playing fields. It seems it could also make a difference in women's representation in company boardrooms and Congress as well.
