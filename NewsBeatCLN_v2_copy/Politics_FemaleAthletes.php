<table bgcolor="#FFFFFF"; style="text-wrap:normal; word-wrap:normal; max-width:730px" cellpadding="10px"><tr><td align="left"><p class="MainText">Sen. Kirsten Gillibrand of New York tries to play tennis a couple of times a week. Sports have been part of her life for a long time, going back to high school when she played tennis and soccer.</p><p class="MainText">

Later, at Dartmouth in the late 1980s, Gillibrand served as co-captain of the squash team. What the future senator did not do in college was participate in student government. "I'd gone to one or two young Democratic events, and interestingly, it was almost all male &ndash; and all of the men were very aggressive," she says. "And so I didn't really feel like I fit in."</p><p  class="MainText">

Obviously, running for office in college makes it much more likely the student will get into public life after graduating. But doing competitive sports may also be a good indicator of political ambition.</p>
