  <p class="Photocaption">Phil Jackson has won a total of 13 NBA championships.</p>
  <hr>
<p class="MainText"><strong>Red Auerbach (16 NBA championships; 9 as a coach, 7 as an executive)</strong></p><p class="MainText">

Of the many Auerbach tales available, one stands particularly tall: Red's singular belief in a young Larry Bird. A college junior with the intent to stay another year when Auerbach drafted him in 1978, Bird was stunned the Boston Celtics would draft someone who wouldn't even take the floor for them in the next season. "Red later told me that a year was not a long time," Bird said, according to Shaun Powell of Sports on Earth.</p><p class="MainText">

Larry Legend, of course, went on to be the linchpin for a Celtics team that helped to bring the NBA to mainstream prominence in the 1980s. The team won three titles and became an indelible brand. Auerbach's unusual investment more than paid off.</p><p class="MainText">

But even more unconventional was a bold move made by Auerbach as far back as 1950. He selected Chuck Cooper in the second round of the draft, making him the first African-American draftee in league history.</p><p class="MainText">

More than tasty historical trivia, this is indicative of Auerbach's sweeping vision. Upon drafting Bill Russell in 1956, Auerbach and the Celtics developed a playing style that revolutionized the way the game was played. Centered around the dynamic pairing of Russell and Bob Cousy, Boston blitzed the league for 11 titles in 13 years with its frontiering style, rich with Russell's all-dominant rim protection and his outlet passes to a scurrying Cousy. Basketball had never seen anything so kinetic.</p><p class="MainText">

And the cigar-chomping eccentricity of Auerbach suggests an individual flair shared by all of these franchise-boosting men. New Yorkers are hoping Jackson's Eastern philosophy-tinged, free-roaming scope and character can be as impactful as Auerbach's uncommon perspectives &ndash; or even as effective as Gregg Popovich's uncanny tongue.</p><p class="MainText">

<strong>Gregg Popovich (5 NBA championships as a head coach)</strong></p><p class="MainText">

Famous in 2014 for his comedic affairs with the media, Popovich has created perhaps the most unique talent harbor of the modern NBA. This year's San Antonio Spurs boasted nine players born outside of America on their roster.</p><p class="MainText">

Past teams have had just as much ethnic diversity. Popovich and Spurs' general manager R.C. Buford have long been lauded for creating a context in the isolated exurban heart of Texas that melds each of their talents into happy cogs, mutually operating the most organic of basketball machines.</p><p class="MainText">

The intensity with which his Spurs have carved up opposing teams' playbooks for a decade-and-a-half suggests Popovich's time spent learning at the United States Air Force Academy wasn't wasted. He's brought an unparalleled sense of consistent precision to his franchise &ndash; work to make any general proud.</p><p class="MainText">

Popovich is no mere boot-camp counselor, though. In addition to his propensity for the silly quip with reporters, he also knows just what tricks of words to use with his men. "I want some nasty!" he famously said to his team, amid a rough 2012 playoff battle with the Oklahoma City Thunder.</p><p class="MainText">

As Popovich seamlessly blends humor with his razor's-edge intensity, it's not hard to see how he charms and brings out the optimal in such a spanning variety of players. His unique approach makes him the chief of a strange basketball island, where everything turns to gold.</p><p class="MainText">

Jackson should aspire to be such a bonding force with the Knicks, where he'll be acquiring well below the amount of superstar talent he's used to. He'll have to squeeze more out of less and create a place like Popovich's, where the broken are fixed.</p><p class="MainText">

Of course, his resume suggests he's up to it.</p><p class="MainText">

<strong>Phil Jackson (13 NBA championships; 2 as a player, 11 as a head coach)</strong></p><p class="MainText">

It remains to be seen how much impact the 68-year-old Jackson will have on his team as an executive, but one thing's for sure: He brings a creative vision that the Knicks have been sorely lacking. He knows a working team concept when he sees one and understands how it extends beyond the roster and what happens on the floor, living through every corridor of the stadium.</p><p class="MainText">

Skepticism about the Knicks hiring Jackson is only warranted because of his age &ndash; unhealthy and weary in his final season coaching with the Los Angeles Lakers, Jackson displayed a rare (for him) reticence for his post that some understandably fear could follow him to his new job.</p><p class="MainText">

But Jackson's intuition for what it takes to win is startling, and if he has even an iota of his rare aptitude left, it'll be a huge plus for New York.</p><p class="MainText"> 

Consider Jackson's famed blackout techniques. He made the Lakers sit in darkened rooms and breathe silently in anticipation of Game 7 of the 2010 NBA Finals (which they won), and he also once made the Bulls practice in a gym with the lights out to prepare them for the chaotic nature of the postseason.</p><p class="MainText"> 

Men who can make winning sense of such strange, gambling, holistic techniques simply don't come around very often. Jackson is one of them.</p><p class="MainText">

Only time will tell how the new hiring pans out &ndash; we should allow Jackson a one-season grace period, at the very least, with which to wade through the much-hyped mess that is the Knicks in 2014.</p><p class="MainText">

But the gesture of bringing the Zen Master in signals an important penchant for correction in New York. They haven't had a society-builder on the level of Jackson since their blue-collar gusto of the 1990s, when Jackson's lifelong rival, Pat Riley, coached the team and played a role in personnel decisions. Those Knicks were consistently one of the chief thorns in the side of Jackson, Michael Jordan and the Bulls' dynasty of the decade.</p><p class="MainText">

<strong>Pat Riley (8 NBA championships; 5 as a head coach, 1 as an assistant, 2 as an executive</strong>)</p><p class="MainText">

"I felt great pressure, every day, to these players to have to walk in and tell them a story that was relevant," Riley said in an interview with Peter Guber. "Storytelling is very important."</p><p class="MainText">

That Riley comprehends the power of narrative &ndash; of the mysteriously gluey nature that lies in language and mythos &ndash; has been elemental to his many achievements. In New York, it was a fervor for the grind that he impressed on his team to make it something fierce. But in 1980s Los Angeles, it was his cooperation with Jerry Buss' "Showtime" vision that turned the Lakers into a unit that transcended mere basketball.</p><p class="MainText"> 

Championships &ndash; important as that team's five of the decade were to it and its city &ndash; seem almost like an afterthought through the hindsight of history. Magic Johnson, Kareem Abdul-Jabbar and the Lakers were so good that they were rock stars, the pillars of NBA's Rome. Surrounded by an organization smart enough to keep them together and glorified, they were driven by something more spectacular than what can be diagrammed on a clipboard.</p><p class="MainText"> 

Riley understood that it wasn't X's and O's, but a careful teasing of the ego that would make Johnson operate best. When the moment called for it, Riley would egg Magic on by dangling Larry Bird's and Michael Jordan's impressive stat lines in front of him before a game. Obviously, the tactic worked; Riley is a master of motivation, one of the premier superstar whisperers of the league's long lore.</p><p class="MainText"> 

***</p><p class="MainText"> 

Knicks fans are praying that Jackson still has some of this brand of magic to sprinkle onto his new team. It's the kind that can make New York relevant to professional basketball again and even potentially help to attract splashy free agents. LeBron James, for one, certainly values the boon of metaphysical qualities a true organizational titan brings to the table.</p><p class="MainText"> 

So beware of quick team fixes and seeming solutions born on the petri dish of the court. History has little regard for such simplicity, instead smiling upon those who take their time to make teams as whole and utopian as can be.</p>
