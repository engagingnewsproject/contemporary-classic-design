<p class="MainText">Since 1959, only 15 teams have won a collective 55 NBA championships. Phil Jackson, Red Auerbach and Pat Riley have been party to 36 of them. Throw in Gregg Popovich, and the number climbs to 40, or 73 percent of all Larry O'Brien Trophies dispersed.</p><p class="MainText">

The history of prized glory in the NBA is the history of brilliant management &ndash; from ownership, to the front office, to the clipboard in the head coach's hand and the level heads he shows it to, titles are earned through top-down organizational cohesion far more than they are on the basketball court.</p><p class="MainText">

Talent is essential, of course, but it's ultimately more replaceable than a gestalt, slowly built culture that aims each of its pieces correctly and maximizes the skills of all members.</p><p class="MainText">

And some individual men &ndash; the New York Knicks' recent hiring of Jackson certainly included &ndash; are the seed for such a structure unto themselves..</p>
