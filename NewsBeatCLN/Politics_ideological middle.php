<table bgcolor="#FFFFFF"; style="text-wrap:normal; word-wrap:normal; max-width:730px" cellpadding="10px"><tr><td align="left"><p class="MainText">It's not news that the ideological middle in Congress is disappearing. But rarely have I ever seen it so starkly documented as in these two slides courtesy of Mehlman Vogel Castagnetti, a lobbying firm here in D.C. Using National Journal's annual vote ratings, the slides compare the most liberal Republican and the most conservative Democrat and count the number of members in between those two poles.</p><p class="MainText">

In the last three decades, the number of members in the middle in the House dropped from 344 (79 percent of the House) in 1982  to four (.9 percent of the House) in 2013.  As the slide suggests, redistricting &ndash; the decennial redrawing of the nation's congressional lines &ndash; plays a major role in that decline. The last two nationwide redraws have largely been incumbent-protection efforts, making Republican districts more Republican and Democratic districts more Democratic. Self-sorting &ndash; the growing tendency of people to live around like-minded people &ndash; is also a major factor in the disappearance of the ideological middle in the House.</p><p  class="MainText">

More intriguing &ndash; and harder to explain &ndash; is how the middle has dropped out of the Senate, which is not subject to redistricting. Because senators represent entire states, self-sorting should be less powerful. And yet, here's the Mehlman Vogel Castagnetti slide on the partisanship in the world's greatest deliberative body.</p>
