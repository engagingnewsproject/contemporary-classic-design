<p  class="MainText">Well more than half of the Senate fit between the most liberal Republican and the most conservative Democrat in 1982. For the last two years, there has not been a single Republican with a more liberal voting record than any Democrat and not a single Democrat with a more conservative voting record than any Republican. Not one.</p><p  class="MainText">

Taken together, there are four &ndash; FOUR &ndash; members of the ideological middle out of the 535 members of the House and Senate combined. That comes out to approximately .7 percent of the entire Congress. In 1982, by way of comparison, more than 75 percent of members of Congress were part of the ideological middle.</p><p  class="MainText">

So, in the last 30 years, the middle has lost more than 99 percent of its membership in Congress. And when the middle is represented by less than 1 percent of the entire Congress, it's not an exaggeration to say the center is gone.
