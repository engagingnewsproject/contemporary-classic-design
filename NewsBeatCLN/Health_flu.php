<table bgcolor="#FFFFFF"; style="text-wrap:normal; word-wrap:normal; max-width:730px" cellpadding="10px"><tr><td align="left"><p class="MainText">Tamiflu, commonly used to reduce flu symptoms, may not work as well as the federal government believed when it spent more than $1.3 billion stockpiling it.</p><p class="MainText">

The Cochrane Collaboration, a nonprofit network of health practitioners, researchers and patient advocates, recently analyzed 46 clinical study reports on Tamiflu and another influenza drug called Relenza to determine their effectiveness.</p><p class="MainText">

The researchers concluded that while both drugs can stop adults' symptoms about half a day earlier, on average, than no treatment, the drugs do not reduce the rate of serious influenza complications, such as hospitalizations and pneumonia. The results were published this week in the British Medical Journal.</p><p class="MainText">

"A significant number of doctors see them as pretty mediocre drugs that don't do a whole bunch," said Dr. Peter Doshi, an assistant professor at the University of Maryland School of Pharmacy and associate editor at BMJ.</p><p class="MainText">

 This isn't news to the FDA, which determined in 1991 that Tamiflu does not reduce influenza-related complications. In fact, the FDA label on Tamiflu packaging says so. It does however, directly oppose one of the Center for Disease Control and Prevention's justifications for stockpiling the drug: that it can reduce complications of the flu.

