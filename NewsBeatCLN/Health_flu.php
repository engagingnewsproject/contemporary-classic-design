<p class="MainText">Additional findings

The Cochrane Collaboration found no significant difference in the time it took for Relenza to alleviate symptoms between adults and children.</p><p class="MainText">

However, it does appear children and adults react differently to Tamiflu. The drug decreases the length of influenza symptoms by 29 hours, on average, in children without asthma. For children with asthma, Tamiflu does not decrease the length of symptoms at all. While the potential benefits of Tamiflu are more potent in children, Doshi said there are also more potential harms.</p><p class="MainText">

When treated with Tamiflu for influenza, 13% of children experienced vomiting, compared to only 8% of children with influenza and no medication. In adults, Tamiflu increased the rate of vomiting by about 4%. The analysis found no side effects in either adults or children who took Relenza.</p><p class="MainText">

"It's a move from the perception that the drugs are risk-free and to the reality that the drugs have some risks," Doshi said of the analysis.</p><p class="MainText">

As for the rate of hospitalizations, there simply isn't enough evidence to suggest that they were reduced by the influenza drugs. Doshi said this may be because for most people, the flu isn't catastrophic. With Tamiflu or Relenza or a placebo, so few influenza patients were hospitalized that there wasn't sufficient evidence to determine if the drugs had an impact.</p><p class="MainText">

The article also addresses the preventative use of Tamiflu and Relenza. While research shows that both Tamiflu and Relenza do decrease the rate of symptomatic influenza, neither seem to decrease the rate of asymptomatic influenza, meaning people taking the drugs could still be spreading the virus.</p><p class="MainText">

This contradicts the belief that preventative treatment with Tamiflu or Relenza could help slow the spread of the virus in a community, which is one reason the government began stockpiling Tamiflu.</p><p class="MainText">

Dr. William Schaffner, a professor of preventive medicine and infectious diseases at Vanderbilt University Medical Center, wishes there was better data on influenza drugs' effectiveness, but says this new article won't influence common practice.</p><p class="MainText">

"Everyone is in agreement that Tamiflu reduces the duration of influenza illness and clearly that's a good thing," Schaffner said.  "The area of  uncertainty is how strong are the data supporting the notion that Tamiflu also reduces the complications of influenza, namely the development of pneumonia,  hospitalizations, intensive care unit admission and death."</p><p class="MainText">

Schaffner says patients admitted to hospitals with influenza should be treated with Tamiflu or Relenza.</p><p class="MainText">

"Should this review change our practices on a clinical level?  I think not really. I think that if anything we ought to be more aggressive with the use of Tamilfu when patients are admitted because these are already very serious influenza infections."</p><p class="MainText">

Dr. Glenn Wortmann, section chief of infectious diseases at MedStar Washington Hospital Center, agrees.</p><p class="MainText">

"For hospitalized patients or patients in the ICU, we would continue to give the drugs because we don't have alternative treatments," Wortmann said.  "I think we've always known that they've not been the most powerful drugs. As far as 10 years ago we knew that they only shortened symptoms by about a day for most patients."</p><p class="MainText">

Clinical study reports are long, in-depth explanations of studies, often ranging from 1,000 to 8,000 pages of material. This is in contrast with medical journal articles, which are typically about 10 pages long. Looking at the full text of the clinical study reports ensures that all data is taken into account.</p><p class="MainText">

"We were looking to get around the problem of reporting bias that can creep into published literature, which occurs when not all studies are produced in the publication," Doshi said.</p><p class="MainText">

Traditionally, clinical study reports are industry secrets, and other than regulators, no one outside the company sees them. The Cochrane Colaboration was given access to the reports by Roche and GlaxoSmithKline, the manufacturers of Tamiflu and Relenza respectively, and was able to determine that the reported decrease in pneumonia &ndash; one of the most common influenza complications &ndash; was based on self-reporting by trial participants, not on actual medical diagnoses.</p><p class="MainText">

The FDA would have had access to these reports, which could explain how it came to a similar conclusion as The Cochrane Collaboration. The CDC, which suggests the drugs can decrease rates of such complications, would likely not have seen the reports.</p><p class="MainText">

The federal government bought a significant amount of the drug &ndash; enough for about 25% of the population &ndash; during the avian and swine flu scares. It hoped to be able to stop the spread of influenza during an epidemic, but there isn't much evidence that it would have worked, Doshi said.</p><p class="MainText">

Roche, Tamiflu's maker, says they stand by the data supporting the efficacy and safety of the drug. According to a statement on their website:</p><p class="MainText">

"Tamiflu has been reviewed and approved by regulatory authorities in over 80 countries, it is recommended as a flu antiviral by public health bodies, including the World Health Organization (WHO) and over 95 million patients have received our drug since it was first made available. We cannot undervalue the role of these authorities in approving and monitoring this or any medicine's use."</p><p class="MainText">

Tamiflu does prevent some flu symptoms, but it's worth discussing with your doctor before you take it.</p><p class="MainText">

Doshi said this research shows the proven benefits of these drugs are "less than believed and assumed by governments" and that "there are harms as well as benefits."
