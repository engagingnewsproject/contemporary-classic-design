  <p class="Photocaption">Joann Herrington holds up the gift certificate to Bobtown Pizza that she and her husband received from Chevron three days after a natural gas well exploded up the hill from their home in Mount Morris, Pa.</p>

  <hr>

<p class="MainText">"I just thought that they was just showing their appreciation of us standing by them with all the trucking, with all the traffic, with all the noise and stuff," Herrington says.</p><p  class="MainText">



Chevron says that's exactly why it gave out coupons for free pizza.</p><p  class="MainText">


In this part of Pennsylvania, the relationship between companies and communities is complicated.</p><p  class="MainText">



Decades before natural gas drilling, coal was king. In the early 1900s, Bobtown became a thriving coal patch on the border of West Virginia. The company owned most of the land in town and built houses for the workers and their families.</p><p  class="MainText">

The biggest mine in town closed in 1993. Now, many of the workers' houses sit abandoned, as does the old company store. Those who grew up in Bobtown still savor the rituals of a close-knit company town.</p><p  class="MainText">

Food is a big part of Bobtown tradition, including the rites of grief: Someone dies, and neighbors bring dinner to the family.</p><p  class="MainText">

"Maybe that's kind of why everybody was so surprised at the reaction to the pizza thing, because we're just used to that. If something happens, you give people food. I never looked at it as a negative thing, you know?" says Bonnie Gansor, who runs the local beauty salon. Her father was a coal miner.</p><p  class="MainText">

But not everyone in Bobtown sees it that way. Julieann Wozniak thinks a multibillion-dollar corporation like Chevron could afford to do even more for the community.</p><p  class="MainText">

"Like we'd be satisfied with pizza coupons for God's sakes," Wozniak says.</p><p  class="MainText">

Wozniak's distrust of big energy companies runs in her blood. Her grandfather was blacklisted for helping organize the mineworkers' union in this part of Pennsylvania.</p><p  class="MainText">

While she supports the petition urging Chevron to apologize, Wozniak says the media attention distracts from the fact that a young worker died in the explosion.</p><p  class="MainText">

"I think an equal attention should be paid to workers' safety. I mean, that was what union organizing was about back in my grandfather's day &mdash; assuring that workers didn't die at a prodigious rate in the mines &mdash; and here we have this new industry and it's the same old, same old," she says.</p><p  class="MainText">


A month after the blast that killed Ian McKee, 27, the cause is still under investigation. Residents in this old coal mining town say they know all too well the risks faced by workers just like him.

