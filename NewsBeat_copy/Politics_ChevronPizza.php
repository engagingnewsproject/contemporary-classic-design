<p class="MainText"><br>More than 12,000 people from the Netherlands to San Francisco have signed a petition demanding that Chevron apologize for insulting Bobtown, Pa., after the energy giant responded to an explosion of one of its natural gas wells by giving nearby residents coupons for free pizza.</p><p class="MainText">



The explosion killed a young worker.</p><p  class="MainText">



Even Stephen Colbert has taken his shots at the so-called Chevron pizza scandal by joking that the gesture was "literally the least they could possibly do." But for residents like Joann Herrington, the whole business isn't registering as a scandal. She lives just down the hill from where the natural gas well exploded.</p>

