  <p class="Photocaption">Google unveiled this prototype self-driving car last May, and is already looking forward to the next version.</p>
  <hr>
<p class="MainText">Google's car uses 64 lasers that scan across 360 degrees, a camera, and GPS map data to generate a map of its surroundings and drive safely. Software models are built in to react to predictable and unpredictable scenarios, according to Urmson.</p><p  class="MainText">

The project director also says that while Google is on the hunt for partners, it is working with automotive suppliers to build a fleet of more advanced "beta" prototypes. Testing of the advanced models is due to begin early this year.</p><p  class="MainText">

At a recent conference, Google co-founder Sergey Brin said he hopes the self-driving car will eventually "transform transportation around the world, and reduce the need for individual car ownership, the need for parking, road congestion and so forth."</p><p  class="MainText">

Urmson said that "it would be goofy for us to try to replicate" the expertise of automakers that already know how to make cars. In other words, Google can supply the technology, while car manufacturers can take care of the build itself, as well as provide expertise in safety and regulatory issues.</p><p  class="MainText">

"We are still assessing the business model," Urmson said. "We don't know what the right relationship is." </p>
