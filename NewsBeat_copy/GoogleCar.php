<p class="MainText">Google is seeking partners to make fully autonomous vehicles a commercial reality within the next five years.</p><p class="MainText">

The Mountain View, California-based company is on the hunt for partners within the car manufacturing industry. Rather than become a car maker itself, the tech giant would prefer to work with others to bring self-driving cars to market. According to the Wall Street Journal, the head of the autonomous vehicle project at Google, Chris Urmson, said: "We don't particularly want to become a car maker. We are talking [with] and looking for partners."</p><p  class="MainText">

In the meantime, the autonomous vehicle project has made progress since the debut of its first prototype. In May last year, Google revealed the purpose-built self-driving car, and prototypes have logged thousands of miles in testing.</p><p  class="MainText">

The autonomous vehicle is intended to work without the standard controls in cars &ndash; such as a steering wheel. Sensors combine with custom software which allows the car to detect hundreds of distinct objects simultaneously &ndash; from pedestrians to stop signs and cyclists &ndash; and then analyzes this data to "act" accordingly.</p>
