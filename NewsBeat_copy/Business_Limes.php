<p class="MainText"><br>Has the price of your margarita cocktail shot up? Guacamole more expensive? Blame it on limes.</p><p class="MainText">



About 98 percent of limes consumed in the U.S. come from Mexico. But our neighbors to the south are feeling seriously squeezed by a shortage of the beloved citrus fruit.</p><p  class="MainText">



At a Mexico City outdoor market, vendor Alberto Reyes Stanislao tries to entice passersby to buy his limes with sweet talk and coaxing. But with limes going for 50 pesos a kilo &mdash; about $1.75 a pound, or three times the normal price &mdash; his sales have plummeted 50 percent. And he says he's getting an earful from customers.</p>

