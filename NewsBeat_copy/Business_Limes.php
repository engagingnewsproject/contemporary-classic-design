  <p class="Photocaption">With reasons ranging from bacterial infections to heavy rains, limes imported from Mexico have shot up in price with some places charging three times as much as the regular prices.</p>

  <hr>

<p class="MainText">"They say I'm selling green gold. One customer told me that instead of eating the limes, she was going to wear them as a necklace," says Reyes.</p><p  class="MainText">



In the U.S., grocery stores are now charging an average of 53 cents for a single lime, compared to 21 cents per fruit at this time last year, according to the latest data from the U.S. Department of Agriculture.</p><p  class="MainText">


A combination of factors has affected Mexican production. Heavy rains late last year in the states of Michoacan, Guerrero and Veracruz hurt the crop. And in Colima, a big lime-producing state, a bacterium is infecting trees.</p><p  class="MainText">

With supply tight at home, exports across the border are getting more and more expensive.</p><p  class="MainText">

"This year, we are in unchartered waters with limes, says Raul Millan of New Jersey-based Vision Import Group. "I've never seen limes at these prices."</p><p  class="MainText">

Millan says he can still recall a time when a 40-pound box of the so-called Mexican Persian variety went for as little as $4. This week, he paid 25 times that much.</p><p  class="MainText">

"I'm surprised that demand is still there, even at $100 a box," he says.</p><p  class="MainText">

With prices that high, Millan says organized crime thieves in Mexico are stealing truckloads of limes. He says his producers in Veracruz have had to hire armed security guards for the trip to the border.</p><p  class="MainText">

In Mexico, authorities say lime production should pick up in the coming months, as yields go up with the arrival of spring, and prices will hopefully fall back down.</p><p  class="MainText">

That's good news to Ruben Jacobo, who's grilling up rib eye, carne asada and chicken at his street taco stand. He says a taco without a squeeze of lime on it is just not a taco. And the high prices are cutting into his already small profit margin.</p><p  class="MainText">

"Unfortunately," Jacobo tells me, "it seems the more expensive something is, the more people want it."</p><p  class="MainText">

Last week, he caught a woman emptying his complimentary plate of lime wedges straight into her bag.

