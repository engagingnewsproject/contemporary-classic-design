  <p class="Photocaption">Electronic pop-band Kraftwerk has produced a 3-D concert.</p>
  <hr>
<p class="MainText">There were giant robotic simulacra of band members for "The Robots," and a welter of numbers flickering and rippling and flying forward in "Numbers." There was a multilayered barrage of neon signs in "Neon Lights" and a shadowy train plunging through darkness in "Trans-Europe Express" &ndash; literalistic, but compelling. During "Spacelab," set in a spaceship cockpit so retro its equipment included reel-to-reel tapes, a drone seemed to be hovering above the theater's front rows; then it revealed itself as a long-needled probe and plunged suddenly into the audience, which gasped. There were surround-sound speakers, too, with voices and bits of synthetic melody popping up from multiple directions.</p><p  class="MainText">

The songs were methodically grouped by topic: computers, city life, transportation (including extended excursions through "Autobahn," "Tour de France" and "Trans-Europe Express") and music itself. They were not new; Kraftwerk's most recent album was "Tour de France Soundtracks" in 2003. Many of the video images were familiar from Kraftwerk's 2012 stint at the Museum of Modern Art, where it played through its catalog of studio albums.</p><p  class="MainText">

Instead of unveiling new material, Kraftwerk has been continually tinkering with its repertory. In various songs, it has beefed up what once were brittle low-resolution sounds, made stiff rhythms funkier and extended some tracks with remixes that incorporate elements of the electronic dance music &ndash; hissing high-hat cymbals, a firm four-on-the-floor thump &ndash; that Kraftwerk presaged. Images were tweaked, too; "Radioactivity," one of the few Kraftwerk songs that is unmistakably a warning, now mentions Fukushima alongside Harrisburg, Pa., and Chernobyl.</p><p  class="MainText">

The songs clicked and blipped, pulsed and swooped, throbbed and crackled. Despite Kraftwerk's dour robotic image, underlined by Ralf Hutter's electronically altered vocals, its songs are still pop, stacking up layers of riff and hook and, particularly in its transportation songs, letting long melody lines rise above the beat. All of its gizmos and programming are still geared to human pleasures &ndash; though new songs would be a pleasure, too.</p>
