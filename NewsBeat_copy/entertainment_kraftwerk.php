<table bgcolor="#FFFFFF"; style="text-wrap:normal; word-wrap:normal; max-width:730px" cellpadding="10px"><tr><td align="left"><p class="MainText">Kraftwerk, the German band whose music predicted pop's electronic future back in the 1970s, hasn't forgotten an old-fashioned precept: A concert is a physical, spatial event. Even as all sorts of experiences &ndash; arts, communication, commerce &ndash; migrate to two-dimensional computer screens, "An Evening With Kraftwerk: 3-D Concert" played up the sensations of a particular performance in a particular room. Kraftwerk's current tour found an ideal setting on Tuesday night as the band started a two-night stand at the ornate United Palace Theater in Washington Heights.</p><p class="MainText">

The band members themselves, in grid-patterned suits, stood impassively behind identical-looking consoles. Behind them was a large video screen; concertgoers had been equipped with 3-D glasses. As the band played its songs &ndash; or generated them, since it was impossible to tell how much was live &ndash; larger-than-life three-dimensional projections towered over the stage and moved into the audience.</p><p class="MainText">

Kraftwerk, founded in 1970, invented itself through a fascination with technology. It has billed itself as "the man-machine," and its songs are equally enthralled and wary of the emotionless precision and pervasive reach of computers, media, high-speed transportation and other inventions with unintended consequences. "Business, Numbers, Money, People," flashed in midair in "Computer World," a song released in 1981 that was tersely prescient about big data and surveillance.</p>
