<table bgcolor="#FFFFFF"; style="text-wrap:normal; word-wrap:normal; max-width:730px" cellpadding="10px"><tr><td align="left"><p class="MainText">Baidu, the operator of China's most widely used Internet search engine, has won the dismissal of a United States lawsuit by pro-democracy activists who complained that the company illegally suppressed political speech.</p><p  class="MainText">

Eight New York writers and video producers had accused Baidu of creating search engine algorithms, at the behest of the Chinese government, to block users in the United States from viewing articles, videos and other information advocating greater democracy in China.</p>

