  <p class="Photocaption">The First Amendment protects Baidu's right to advocate for systems of government other than democracy (in China or elsewhere) just as surely as it protects plaintiffs' rights to advocate for democracy, US District Judge Jesse Furman wrote.</p>

  <hr>

<p class="MainText">The plaintiffs said this kept Baidu users from seeing the plaintiffs' work, unlike users of other search engines such as Google and Microsoft's Bing. They sought $16 million in damages for violations of their civil and equal protection rights.</p><p  class="MainText">

United States District Judge Jesse M. Furman in Manhattan concluded Thursday that the results produced by Baidu's search engine constituted protected free speech under the First Amendment of the United States Constitution, warranting dismissal of the lawsuit, filed in May 2011.</p><p  class="MainText">

"The First Amendment protects Baidu's right to advocate for systems of government other than democracy (in China or elsewhere) just as surely as it protects plaintiffs' rights to advocate for democracy," the judge wrote.</p><p  class="MainText">

Judge Furman likened a search engine's "editorial judgment" to that of a newspaper editor who decides which stories to publish.</p><p  class="MainText">

He also said there is a "strong argument" that the First Amendment immunizes search results from "most, if not all" kinds of civil liability and government regulation.</p><p  class="MainText">

"To allow plaintiffs' suit to proceed, let alone to hold Baidu liable for its editorial judgments, would contravene the principle upon which our political system and cultural life rest: That each person should decide for himself or herself the ideas and beliefs deserving of expression, consideration and adherence," the judge said.</p><p  class="MainText">

Stephen Preziosi, a lawyer for the activists, said his clients would appeal. "The court has laid out a perfect paradox: That it will allow the suppression of free speech, in the name of free speech," he said in an interview.</p><p  class="MainText">

Mr. Preziosi also criticized Judge Furman's newspaper editor comparison, calling Baidu "more analogous to a town square, where pretty much anyone can go and say what he wants."</p><p  class="MainText">

Baidu declined to provide immediate comment.</p><p  class="MainText">

The importance of the decision is that Baidu, along with other search engines like Google, Yahoo and Bing, has the same editorial rights as print publications and can choose whether or not to publish people's writings, said Carey Ramos, of Quinn Emanuel Urquhart & Sullivan, Baidu's lead attorney for the case.</p><p  class="MainText">

"That rights extends to Internet media as well as print media. And it protects Chinese media as much as American media," Mr. Ramos said. "The plaintiffs sued Baidu saying, 'You're violating our First Amendment rights because you don't include our writings in your search results,' but the court said, 'No, you've got it backwards, your lawsuit violates Baidu's First Amendment rights by asking the court to penalize Baidu for not returning the results you want.' "</p><p  class="MainText">

China's Foreign Ministry said online operators followed the country's rules.</p><p  class="MainText">

"The Chinese government consistently guarantees in accordance with the law Chinese citizens' freedom of speech, and consistently demands that Chinese Internet companies strictly enforce relevant Chinese laws and rules," said Hong Lei, a spokesman for the ministry. "The relevant judgment just so happens to affirm Chinese government policy."</p><p  class="MainText">

The lawsuit was filed one year after Google pulled its search engine out of mainland China after encountering censorship problems. China has also blocked Google's YouTube service and social networking sites such as Facebook and Twitter.</p><p  class="MainText">

In March 2013, Judge Furman had dismissed the lawsuit on procedural grounds but later allowed the case to resume.</p>

