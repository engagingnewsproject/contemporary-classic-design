  <strong>U.S. Judge Dismisses Lawsuit Against Chinese Search Engine</strong></span>

<p class="Photocaption" align="left">BY ALYSSA GOODMEN</p>

<p ><img src="Print.jpg" alt="" width="181" height="31" /></p>

