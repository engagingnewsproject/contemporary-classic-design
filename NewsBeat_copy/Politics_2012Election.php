<table bgcolor="#FFFFFF"; style="text-wrap:normal; word-wrap:normal; max-width:730px" cellpadding="10px"><tr><td align="left"><p class="MainText">For all the criticism about long lines and other Election Day snafus, most states actually improved the way they handled elections between 2008 and 2012, according to a new study from the Pew Charitable Trusts.</p><p class="MainText">

The report found that, overall, wait times at polling stations decreased by about three minutes over 2008, and 40 states and the District of Columbia improved their "election performance index" scores, which Pew calculated from 17 indicators that make up the index.</p><p  class="MainText">

Pew analyzed state election administration by looking at factors such as the availability of voting tools online, voter turnout, wait times at polling stations and problems with registration or absentee ballots.</p>
