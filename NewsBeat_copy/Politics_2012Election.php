<p class="MainText">Of the 10 states with the longest average wait times, eight of them were in the South &ndash; including Florida, which had the longest wait time in the nation in 2012 at 45 minutes.</p><p  class="MainText">

Although voter turnout was lower in 2012 than in 2008, fewer people who did not vote said they were deterred from voting by illness, disability, or problems with registration or absentee ballots.</p><p  class="MainText">

"States are pioneering innovations that make a real difference in the efficiency and accuracy of their elections operations while also saving money," said Pew's David Becker, in a statement.</p><p  class="MainText">

He told NPR that a growing number of states allow online voter registration and use technology to make voting more efficient. There are currently 17 states that offer voters the opportunity to register online &ndash; 13 states offered online registration before the 2012 election and four states have added it since then.
