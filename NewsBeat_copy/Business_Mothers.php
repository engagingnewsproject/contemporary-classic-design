  <p class="Photocaption">The percent of women who "stay at home" and don't work outside the home has risen to 29 percent, up from 23 percent in 1999, according to a study by the Pew Research Center</p>

  <hr>

<p class="MainText">"Stay at home" mothers includes women who remain in the home to care for family as well as those who say they don't work outside because they are unable to find work, are disabled or enrolled in school.</p><p  class="MainText">



According to Pew: "The largest share consists of 'traditional' married stay-at-home mothers with working husbands. They made up roughly two-thirds of the nation's 10.4 million stay-at-home mothers in 2012. In addition to this group, some stay-at-home mothers are single, cohabiting or married with a husband who does not work."</p><p  class="MainText">


The survey, published Tuesday, notes that 60 percent of Americans say children are better off with a parent at home, while 35 percent say they are just as well off if both parents work. Hispanics, white evangelical Protestants and those who never attended college are more likely to say a parent should be at home, while college-educated women are more likely to say it doesn't make a difference.</p><p  class="MainText">

Pew's study also shows that stay-at-home moms tend to be younger, poorer and less educated than their working counterparts. And while married stay-at-home mothers with a working husband were far less likely to live in poverty (15 percent) than those who were married to nonworking husbands (74 percent) or were cohabiting (88 percent), single mothers (71 percent) actually fared slightly better than the latter two groups.</p><p  class="MainText">

However, married mothers with working husbands were considerably more likely to be stay-at-home by choice, with 85 percent saying they did not work outside in order to care for family. Single mothers were least likely to say that (41 percent).

