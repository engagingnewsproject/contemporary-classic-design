<p class="MainText">In a Q&A, Howard opens up about why soccer struggles in the U.S., his marriage, and how having Tourette's and OCD may have helped him.</p><p  class="MainText">

<strong>You talk about the difference between the U.S. and other countries, in that here soccer is a middle class and above sport, whereas in the rest of the world it's something else.</strong></p>
<p class="MainText">

Soccer in the rest of the world is what we know as basketball. It's a pickup game. There's not a lot of rules. You don't need a field, you don't need a goal. You can make it up as you go. That's what basketball is for us &ndash; you just need a ball and a hoop. If you have a friend, great. But if not you can still play the game. That's what it looks like in Asia, Africa, South America, Europe. In the U.S., where I grew up playing, it wasn't just go get the ball and work on your moves. It was, we have to have a team, all the right balls, a big field, and everything has to look right and be right. In the rest of the world it's a street game, and that's why the rest of the world developed so much more quickly.</p><p  class="MainText">

<strong>In the book you write about your struggles, particularly as an adolescent, dealing with Tourette's syndrome and OCD. What was the hardest part of it growing up?</strong></p><p class="MainText">

The hardest part was the unknown of the tics. Right now, as I've gotten older, my tics sustain for five or ten years. So, I can deal with them on a daily basis, I know how it affects my body. But when you're 10 years old, and every three months a tic comes along, it's daunting because you don't know what the next one is going to look like, what it's going to feel like. You know your body's changing and developing new habits, and it's hard to keep up with that.</p><p  class="MainText">

<strong>You also say that you think your condition gave you an advantage in some ways.</strong></p><p class="MainText">

In some roundabout way it allowed me to be hyper-focused when I'm in the game. It's particularly hard to explain it, other than that I was so zoned in to the game, to the ball, to the crowd. It's given me that laser-type focus.</p><p  class="MainText">

<strong>There is a brutally honest section of the book about how you fell out of love with your wife, and essentially chose soccer. Why did you want to include that?</strong></p><p class="MainText">

I think it was, again, part of my story. Certainly one that I've never shared. I'm very guarded about my personal life. It was a major incident in my life, and so I'd be remiss to not talk about it. I think there is some hope in that story. I talk about why it happened. You look at success on one hand with soccer, but that came at a price, and I think it's important to see how that balance works. In my life, I wasn't able to have both, and I think a lot of athletes struggle with that. It was something I wanted to talk about. But again, there's redemption, there's hope. Life looks great for our family moving forward. There is a positive story in there.</p><p  class="MainText">

<strong>What image are you hoping people who pick up this book and read it, come away with?</strong></p><p class="MainText">

I think I'm a guy who on TV just yells and rants and raves and makes saves, but there's another side to me, and there always has been. I'm human like everyone else, but I try to stay grounded. Hopefully they'll get that from the story. When you look at the Young Adult version, <i>The Unguarded Story of Tim Howard</i>, I think there's something there for children. For kids with obstacles. Every kid has obstacles, whether it's Tourette's or something else, I wasn't any different and that's what you see. I'm here now, but I was just a kid who wanted to play soccer, there was nothing special about me. I feel like people can kind of latch on to that and think that there's some hope there. Like I said, in spite of or because of my circumstances, I was able to accomplish my dreams. </p>
