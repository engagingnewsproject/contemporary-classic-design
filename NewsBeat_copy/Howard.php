<p class="MainText">Tim Howard set a World Cup record for saves in the U.S.'s heartbreaking loss to Belgium in 2014. His bearded face of pure intensity on the field as well as his on-field moxy came to symbolize a new age of American soccer.</p><p class="MainText">

Now, the goalkeeper is out with a memoir about his life until that point: <i>The Keeper: A Life of Saving Goals and Achieving Them</i>.</p><p  class="MainText">

The book details his life growing up in New Jersey. His mother is an immigrant from Hungary, his father, who left when he was young, "is black, a Woodstock hippie turned long-haul trucker." Howard struggled with Tourette's syndrome and OCD.</p><p  class="MainText">

Thanks to its honesty, the book is one of the best sports memoirs I've read recently. His section on why he and his wife split would be risky if its candor were not so refreshing. His explanation of Tourette's is eye-opening and educational.</p>
