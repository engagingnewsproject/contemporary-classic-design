<table bgcolor="#FFFFFF"; style="text-wrap:normal; word-wrap:normal; max-width:730px" cellpadding="10px"><tr><td align="left"><p class="MainText">As electronic cigarettes increase in popularity, calls to the nation's poison control centers about exposure to the liquid nicotine used in many of the devices have surged, the Centers for Disease Control and Prevention said Thursday.

In February, there were 215 poison center calls involving e-cigarettes, the CDC said in its Morbidity and Mortality Weekly Report. That's compared to one per month in September 2010.

And 51% of those calls involved children 5 and under, officials said.
