<p class="MainText">For Bud Selig, Derek Jeter is baseball's perfect knight.</p><p class="MainText">

During a recent conference call to promote the relationship between MLB and ESPN, Selig proclaimed Jeter as the player who best represented the sport during Selig's 22-year tenure as commissioner.</p><p class="MainText">

"I'll have plenty to say during the course of the year, but no player in my time has represented this sport any better than Derek Jeter," Selig said. "He really has, in many ways, been the face of baseball, and I am proud of him. I've told him that often. He's just been a great player on the field, but to be frank with you, a better person off the field."</p>
