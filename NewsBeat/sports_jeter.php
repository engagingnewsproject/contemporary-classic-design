  <p class="Photocaption"> Derek Jeter has won five World Series rings with the New York Yankees.</p>
  <hr>
<p class="MainText">Baseball has plans to celebrate Jeter's final year as a player as it did last year with Mariano Rivera. The 39-year-old Jeter announced in February that he will retire after the 2014 season.</p><p class="MainText">

"There will be a lot of appropriate celebrations as time goes on, and we're talking about a lot of things now, but a lot of that also is going to be up to Derek," Selig said. "I'm sorry to see him go, but in every great career has to come to an end at some point. You couldn't have done any better as a human being the last 20 years relative to this sport than Derek Jeter has done."</p><p class="MainText">
 
Selig also used the conference call to highlight that ESPN (the network has a new eight-year, $5.6 billion deal that gives it more flexibility in picking games) will air every team at least once this season on its national Sunday Night Baseball program &ndash; the network could air as many as 101 games this season, including the playoffs. He also addressed baseball's blackout policy which aims (in baseball's words) to protect local markets.</p><p class="MainText">
 
"We have, I think, the most fair blackout policy, and I don't even like to use the term blackout," Selig said. "It's in a very, very limited area, and the fact is that my goal has always been to protect the local market and the local television carriers ... It's just fair. Local clubs make local deals, and therefore it's up to us, both morally as well as economically, to protect that deal. Yes, we continue to work on that policy. I'll get letters occasionally from people, and I always respond. We try to be as fair as we can, and by the way, we will continue to do that."
