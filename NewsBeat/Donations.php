<p class="MainText">The national political parties and some of their biggest donors are embracing a new law that dramatically increases contribution limits, saying it could help the parties stage a financial comeback in an era of unlimited spending by super PACs.</p><p class="MainText">

Some third-party groups on the left and the right of the political spectrum aren't as pleased, warning that the national parties will have more power to drown out upstart politicians challenging the establishment's favored candidates.</p><p  class="MainText">

They all agree on one point: More campaign money will start to slosh through federal elections &ndash; just as the 2016 presidential campaign heats up.</p><p  class="MainText">

"The pit of money just got more bottomless," Kenneth Gross, a leading Washington election lawyer, said of the new contribution limits Congress tucked into a $1 trillion spending bill President Obama recently signed into law.</p>
