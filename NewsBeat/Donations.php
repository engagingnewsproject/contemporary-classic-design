<p class="MainText">Previously, an individual could not give more than $32,400 to a single party committee, such as the Republican National Committee, each year. The new law allows the same donor to contribute three times that amount to each of eight party committees. As a result, an individual can give nearly $1.6 million to a national party and its various branches in a two-year election cycle.</p><p  class="MainText">

Republican National Committee Chairman Reince Priebus has hailed the change, noting that donations to the party are publicly disclosed. "The new policy will increase transparency and fairness in political fundraising," he said.</p><p  class="MainText">

On the left, the Progressive Change Campaign Committee's spokeswoman Laura Friedenbach said, "Gutting campaign-finance laws represents Democrats marching in the exact wrong direction."</p><p  class="MainText">

And even as the parties gain more financial clout, those helping run super PACs say they don't expect outside groups to retreat. Chris Lehane, a Democratic strategist and top adviser to billionaire environmentalist Tom Steyer, said Steyer won't end his mission to make climate change a central issue in elections. Steyer put more than $70 million into his own super PAC to influence last year's midterms. Most of his favored candidates lost. Lehane said the "big oil Goliaths" won't stop spending in politics and neither can environmentalists.</p><p  class="MainText">

"Until there's a change, we are, unfortunately, on a path where more and more money will find its way into the system," he said. </p>
