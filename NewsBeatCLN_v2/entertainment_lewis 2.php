<p class="MainText"><br><i>The author of "Liar's Poker," "The Blind Side" and, most recently, "Flash Boys" says it's hard to dramatize the quotidian in a way that makes it fresh for readers: "It's like describing the air we breathe."</i></p><p class="MainText">

<b>What books are currently on your night stand?</b></p><p class="MainText">

More stuff ends up on my night stand than I'll ever read. Every night when I go to bed I pass two giant towers of literature. One of them consists of books I want to read for no particular reason. On top of this tower at the moment are two novels, Neil Gaiman's "American Gods" and Christine Sneed's "Little Known Facts." The other tower is built of books I feel that I need to read for something I'm working on. At the moment, I need to know a lot more than I do about the period in U.S. history between 1918 and 1929, and so the work tower is topped by Sinclair Lewis's "Main Street"; David Kyvig's incredibly good history, "Daily Life in the United States, 1920-1940"; and John Brooks's "Once in Golconda." The weirdest books in either stack are the United States censuses of 1920 and 1930. </p><p class="MainText">


<b>What was the last truly great book you read? </b></p><p class="MainText">

Dave Eggers's novel "A Hologram for the King." I don't know that the picture Eggers draws of Alan Clay is true to the inner life of the ordinary American businessman. But it feels true, in the same way that Willy Loman feels true. It's just very hard to do what Eggers does in that book &ndash; to dramatize the quotidian in a way that causes a reader to see it fresh. It's like describing the air we breathe.</p>

