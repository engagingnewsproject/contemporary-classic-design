<p class="MainText"><b>Sell us on your favorite overlooked or underappreciated writer.</b></p><p class="MainText">

There are a bunch of them. My favorite is Jim Holt &ndash; though his recent book "Why Does the World Exist?" hasn't really been overlooked. In the early 1990s, Holt wrote a regular column for Literary Review, the British magazine, that was so good I just assumed "Jim Holt" must be a pseudonym for some seriously famous British writer. I couldn't believe there was actually a human being &ndash; an American &ndash; named Jim Holt whom no one had heard of. I don't understand why Jim Holt has not become seriously famous. He's a total delight on the page. Every time he writes something, I'm reminded of the pleasure of reading. </p><p class="MainText">

I'd like also to include a subcategory: the overlooked or underappreciated work of an otherwise appreciated writer. Writers have their moments; they rise to occasions; they go on hot streaks. In his writing about very old age for The New Yorker, Roger Angell is right now having a hot streak. I hope he lives forever so that he can write about it. </p><p class="MainText">

<b>What are your literary guilty pleasures? Do you have a favorite genre?</b></p><p class="MainText">

I've never felt guilty reading a book. My literary guilty pleasure is television. My guiltiest pleasure is the HBO series "Eastbound & Down." The writers behind it probably don't think of their talent as literary, but it is. When I watch it, I feel a little bit the way I did when I was first reading "Portnoy's Complaint." </p><p class="MainText">

<b>Which books might we be surprised to find on your shelves?</b></p><p class="MainText">

The United States censuses from 1920 and 1930.</p><p class="MainText"> 

<b>What's the best book on Wall Street you've ever read?</b></p><p class="MainText">

"Reminiscences of a Stock Operator," by Edwin Lefèvre, a thinly veiled biography of Jesse Livermore, the speculator, most famous for betting against the U.S. stock market before the crash of 1929. What he should be most famous for is this book. That something first published in 1923 remains so relevant and readable tells you how little Wall Street has changed in the last century. (By contrast, it's hard to imagine a book from even 30 years earlier feeling so fresh.) It's crammed with truth about market life: "Nobody can make big money on what someone else tells him to do." </p><p class="MainText">

<b>You studied art history at Princeton before getting a master's in finance. Do you enjoy reading books about art or art history? Any contemporary recommendations or favorites from your student days?</b></p><p class="MainText">

I should, if only to keep up appearances. I loved the memoirs of the early French art dealers — Ambroise Vollard's "Recollections of a Picture Dealer" is the best of them. Also, basically everything written by E.H. Gombrich.</p><p class="MainText">

<b>What was the last book that made you laugh?</b></p><p class="MainText">

Mordecai Richler's "The Apprenticeship of Duddy Kravitz."</p><p class="MainText">

<b>What was the last book that made you cry?</b></p><p class="MainText">

"Charlotte's Web."</p><p class="MainText">

<b>What kind of reader were you as a child? And what were your favorite childhood books? </b></p><p class="MainText">

The first books I really ripped through were the Hardy Boys mysteries. Around age 11, I began to resist the books that my teachers required me to read, in favor of books I suspected my teachers would disapprove of. I read "Rosemary's Baby" before "To Kill a Mockingbird." I learned of the existence of masturbation from "Portnoy's Complaint" &ndash; which I read only because I sensed my parents had tried to hide it. Philip Roth still occupies an unusual place in my heart.</p><p class="MainText">

<b>Whom do you consider your literary heroes?</b></p><p class="MainText">

In the order in which I encountered them: Mark Twain. Walker Percy. George Plimpton. Tom Wolfe. John McPhee. F. Scott Fitzgerald. Saul Bellow. Vladimir Nabokov. Rebecca West. George Orwell, of the essays but not so much the novels. Alan Bennett. Neil Gaiman.</p><p class="MainText">

<b>Which novels have had the most impact on you as a writer? Is there a particular book that made you want to write?</b>

It's a weird list, and the influence of the books on it may have more to do with when I encountered them than anything else: "Huckleberry Finn," "The Moviegoer," "Great Expectations," "The Right Stuff," "Lucky Jim" and "A Confederacy of Dunces." It would never have occurred to me to write a book when I first read "Huckleberry Finn." But I remember wishing, if only vaguely, that I might be able to create the pleasure I took from that book for myself.
</p>

