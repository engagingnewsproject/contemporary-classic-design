<p class="MainText"><br>A Denver District Court judge on Wednesday tossed out a lawsuit filed by a group of mostly Denver hotels against Aurora's Gaylord Rockies Hotel and Conference Center, clearing the way for the 1,500-room hotel and water park near Denver International Airport.</p><p class="MainText">

Judge Norman Haglund ruled that none of the claims by the plaintiffs to block the project had standing. In the lawsuit, the hotel group had sought to overturn the $81.4 million award to Aurora under the Regional Tourism Act by the Colorado Economic Development Commission in 2012.</p><p  class="MainText">

"We're very pleased with the judge's decision, and we obviously agree with him," said attorney Thomas Snyder, who represented Aurora in the legal battle.</p>
