<p class="MainText">Attorneys for the plaintiffs said they will decide whether to appeal the ruling.</p><p  class="MainText">

"The judge has made legal conclusions that the plaintiffs disagree with, and we'll be reviewing our options in terms of an appeal," said Hilary Wells, who represents the hoteliers.</p><p  class="MainText">

Depending on the outcome of another lawsuit, construction could start on the $750 million to $850 million Gaylord project sometime next year, with an opening in 2017. It would be located on 64th Avenue between Tower Road and E-470."</p><p  class="MainText">

Aurora Interim City Attorney Mike Hyman said the city still plans to seek damages from the hoteliers in a separate lawsuit filed against the hotel group in Arapahoe County District Court. And another lawsuit filed by two Aurora residents still is active. The two sued on the grounds that local tax incentives awarded to the Gaylord Rockies hotel violate TABOR, the state's Taxpayer's Bill of Rights.</p><p  class="MainText">

But Wednesday's ruling was a key hurdle in getting the Gaylord project started, officials said.</p><p  class="MainText">

The lawsuit filed on behalf of the 11 hotel owners claimed that Aurora should have had to reapply for the tourism tax because the proposal changed developers and operators after Aurora was awarded the money.</p><p  class="MainText">

It also claimed the "injury" a new competitor would create to the existing hotel market should not have been funded with the state subsidy because it wasn't necessary for the project.</p><p  class="MainText">

But Judge Haglund said the hoteliers did not have standing to make those and other claims.</p><p  class="MainText">

The plaintiffs included the owners of the Curtis, the Broadmoor, the Brown Palace, the JW Marriott Denver Cherry Creek, the Courtyard Denver Downtown, the Magnolia, the Oxford and the Westin Westminster hotels.</p><p  class="MainText">

