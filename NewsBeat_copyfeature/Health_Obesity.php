<table bgcolor="#FFFFFF"; style="text-wrap:normal; word-wrap:normal; max-width:730px" cellpadding="10px"><tr><td align="left"><p class="MainText">A government study found that the obesity rate for children ages 2 to 5 dropped by about 40 percent between 2003 and 2012. That comports with another recent government report finding that, in several states, young, poor children are healthier. This is encouraging but no reason for complacency. In fact, last week's announcement shows that obesity remains a daunting problem that demands more effort &ndash; from Americans and their government.</p><p  class="MainText">

Assuming the results reflect a real and significant trend, what will happen when these children grow up? For Americans older than 5, obesity rates have stayed about the same in recent years. A third of adults are obese, and 17 percent of all children are morbidly overweight. The positive spin on these findings is that Americans' waistlines have finally ceased to expand. We'd suggest that the country should not be comfortable that the ravages of obesity &ndash; diabetes, heart disease, hypertension, cancer and more &ndash; will diminish the living standards of merely a third of our countrymen, even if some of their children and grandchildren end up better off. 
</p>

