<p class="MainText">Meanwhile, maintaining even a diminished quality of life for overweight people is expensive. Researchers in 2012 estimated that keeping the obesity rate steady would save some $550 billion over the next two decades, compared to scenarios public-health experts feared. But that would still leave annual spending on treating obesity-related disease at about $150 billion a year.</p>
<p  class="MainText">

The government should stop subsidizing crops such as corn, which gets converted into high-fructose corn syrup. It should conduct more research on what's behind the plateauing obesity rate and the improvement in toddlers. New labeling requirements for store-bought food, proposed last week by the Food and Drug Administration, will help. They will make it harder for food companies to confuse consumers with unrealistic serving sizes and will list added sugars, a major contributor to poor nutritional health. The FDA could do better &ndash; for example, by giving consumers a sense of how much sugar is too much sugar &ndash; but the added transparency will be a good step.</p>
<p  class="MainText"> 

More difficult to implement, but probably more effective, would be direct financial incentives. Many public-health activists want to tax sugary drinks. Understandable, but studies and simple reasoning argue for taxing the underlying culprit, sugar, wherever it appears. A partial step would be to reform the food stamp program to promote healthier eating, as has already happened in a similar program.</p>
<p  class="MainText"> 

All of the burden can't fall on government. Health insurers have begun to take a bigger role in encouraging physical activity and reasonable diets, including by adjusting premiums or other payments. They should continue looking for ways to reward people who improve their lifestyles. That could help insurers' bottom line &ndash; and the well-being of their customers. 
</p>
