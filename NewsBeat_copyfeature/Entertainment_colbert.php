<p class="MainText"><br>The real Stephen Colbert is probably someone "Stephen Colbert," the character, would find easy to mock.</p><p class="MainText">
 
The real Colbert is reportedly just a regular guy who lives in a New Jersey suburb with his wife and three kids, has taught Sunday school and says earnest things like "I really admire newsmen" when explaining that he doesn't confuse his Colbert character with an actual journalist.</p><p class="MainText">
 
Fans have readily embraced the blustering buffoonery of the conservative character Colbert, who arrives on the "Colbert Report" set to calls of "Stephen! Stephen!" It remains to be seen if they will have equal affection for the man Colbert who will replace David Letterman as host of "The Late Show" in 2015.</p><p class="MainText">
 
Even Colbert the character is aware of how tough that just might be.
</p><p class="MainText">

"I gotta tell you, I do not envy whoever they try to put in that chair," he said with a wink on a recent show as the audience cheered. "Those are some huge shoes to fill."</p><p  class="MainText">
 
If anyone is used to challenges, it's Stephen Colbert. Raised in Charleston, South Carolina, the youngest of 11 children, he was just 10 years old when his father and two of his brothers were killed in a plane crash in 1974. Eastern Airlines Flight 212 crashed in a cornfield near Charlotte, North Carolina, and 72 people were killed.</p><p  class="MainText">
 
Colbert told Oprah Winfrey it took him years to grieve their deaths.</p><p  class="MainText">
 
"I didn't really really feel the loss until I was in college," he said " Then, oh, I was in bad shape. "</p><p  class="MainText">
 
He told the New York Times Magazine in 2012 that he credits his mother with helping him survive the tragedy.</p>
