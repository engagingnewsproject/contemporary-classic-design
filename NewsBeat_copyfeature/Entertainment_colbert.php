  <p class="Photocaption">How will the goofy Colbert adjust to his new roll?</p>
  <hr>
<p class="MainText">Colbert says it was his mother who encouraged him to enter Northwestern's theater program, which led him into improv and a gig with Chicago's famous Second City troupe. It was there he struck up friendships with Steve Carell and Amy Sedaris.</p><p  class="MainText">
 
Colbert eventually landed in New York City where he briefly worked with ABC's "Good Morning America" as they needed someone who "looked straight, but could act funny," he told CNN's "Larry King Live" in 2007.</p><p  class="MainText">
 
"They really just wanted me to quip," he said. "I did one piece for them."</p><p  class="MainText">
 
 "GMA" shot down several other story ideas he pitched, Colbert said, but his credentials from them came in handy when "The Daily Show" came calling in 1997 looking for correspondents. Colbert had written a little for "Saturday Night Live" and his combination of comedy chops and the brief experience working with "GMA" made him "genetically engineered" to work with the show, he said he was told.</p><p  class="MainText">
 
Thus, the character of "Stephen Colbert" was introduced to the world.</p><p  class="MainText">
 
One person who could do without his alter ego is his wife, Evelyn "Evie" McGee-Colbert, whom he married in 1993. She told Oprah Winfrey that other Colbert is not welcomed in their home.</p><p  class="MainText">
 
"I don't really like the other guy," she said. "He doesn't come in this house."</p><p  class="MainText">
 
Her husband is smart, funny and incredibly romantic, McGee-Colbert said. He is also intensely private, though he works hard to connect with his audiences and fans, he told Rolling Stone Magazine.</p><p  class="MainText">
 
"I was 22 or 23 when I made a decision not to be actively Hamlet-like and miserable in my daily life, and the decision helped a lot," he said. "Living vitally is not easier than living morbidly -- it's just better. People are all we've got."</p><p  class="MainText">
 
Colbert will have to rely on such fans to make his new venture a success. Merissa Marr of the Wall Street Journal wonders at his crossover appeal once Colbert the character has been vanquished.</p><p  class="MainText">
 
"Whether Mr. Colbert can maintain his following as a different persona, no longer cloaked in parody, isn't guaranteed," she writes, noting that "CBS is arguably the most middle of the road of the broadcast networks."</p><p  class="MainText">
 
Colbert's old boss has no worries. On "The Daily Show," Jon Stewart said, "The exciting news is that I no longer need a cable subscription for the privilege of watching Stephen Colbert."</p><p  class="MainText">
 
"I think Stephen Colbert is up for the challenge," Stewart said. "So while we wish Dave the absolute best for a well-earned retirement, there's no greater joy for seeing a genuinely good man, who works as hard as he can every day and deserves all the success in the world, actually get that success. For Stephen we're just thrilled."
