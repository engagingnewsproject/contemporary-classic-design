<p class="MainText">The woman, whose name wasn't released, suffered from severe headaches due to a thickening of her skull. She slowly lost her vision, her motor coordination was suffering and it was only a matter of time before other essential brain functions would have atrophied, Verweij said in a press release issued by UMC Utrecht.</p><p  class="MainText">

Verweij noted that in some brain operations it's common for part of the skull to be temporarily removed to reduce pressure on the brain, then put back later or replaced by an artificial implant. In this case, doctors inserted nearly an entire plastic skull that was manufactured with the help of Anatomics, an Australian medical device company that specializes in 3-D printing.</p><p  class="MainText">

"We used to create an implant by hand in the operating theater using a kind of cement, but those implants did not have a very good fit," Verweij said. "Now we can use 3-D printing to ensure that these components are an exact fit. This has major advantages, not only cosmetically but also because patients often have better brain function compared with the old method."</p><p  class="MainText">

Three months after surgery, the woman's pain is gone and she can see again.</p><p  class="MainText">

"The patient has fully regained her vision, she has no more complaints, she's gone back to work and there are almost no traces that she had any surgery at all," said Verweij.
