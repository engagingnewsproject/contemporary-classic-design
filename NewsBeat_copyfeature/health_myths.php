<p class="MainText">Myth: Crunches are the key to flat abs.</p><p  class="MainText">  Fact: They may be the most iconic abdominal exercise around, but doing crunches is not actually the best way to slim your midsection.</p><p  class="MainText">"Since they don't burn off a lot of calories, they don't help in a major way with fat loss," says Wayne Westcott, professor of exercise science at Quincy College in Quincy, Massachusetts.

And while crunches do tone a small portion of your abs, moves involving your distal trunk &ndash; which includes your shoulders and butt &ndash; more effectively engage your entire core, according to a study from Pennsylvania State University. So you'll whittle your waist far more dramatically by doing planks and bridges (and more of these fat-burning ab exercises).</p><p  class="MainText">

If you are doing crunches, make sure to use proper form: Otherwise, they may put your spine in a painful curved position.</p><p  class="MainText">



Myth: The more you sweat, the more you burn.</p><p  class="MainText">

Fact: Especially drenched after your regular afternoon run? That doesn't mean you necessarily torched any more calories than usual (sorry!).</p><p  class="MainText">

"Sweat is a biological response that cools your skin and regulates internal body temperature," Matthews says. It's just as apt to be the result of an overheated studio, the weather or your personal physiology as it is a grueling gym session.</p><p  class="MainText">

Myth: Running is bad for your knees.</p><p  class="MainText">

Fact: A Stanford University study found that older runners' knees were no less healthy than those of people who don't run. But while pounding the pavement is safer on the joints than contact sports like football, it's not totally harmless.</p><p  class="MainText">

"Women are four to six times as likely to be at risk of serious knee injuries from running as men, because they tend to have an imbalance in the strength ratio between their quadriceps and hamstrings, which can increase the risk of ACL injuries," Westcott says.</p><p  class="MainText">

That's why experts recommend doing a total-body strength workout at least twice a week in addition to your regular jogs to build up the muscles that support the knees. "You will enhance your running experience and also reduce your chances of getting injured," Matthews points out.</p><p  class="MainText">



Myth: Stretching helps your body recover faster.</p><p  class="MainText">

Fact: Keep doing it if it feels good to you, but a recent University of Milan study on the effects of post-workout recovery methods found no significant changes in blood lactate levels (a measure of how fatigued your muscles are) in folks who stretch after exercise.</p><p  class="MainText">

While stretching may not completely reduce muscle soreness or speed muscle tissue repair, limbering up still has certain benefits, Westcott says: Doing it right after a workout, when the body is still warm, is the best way to increase joint flexibility.</p><p  class="MainText">

Myth: You need to sweat for 45 minutes to get a health benefit.</p><p  class="MainText">

Fact: Even if you've got just a half-hour to spare a day &ndash; or a mere 10 minutes &ndash; you have enough time to bolster your cardiovascular health.</p><p  class="MainText">

More and more studies are pointing to the power of short workouts &ndash; and some even suggest that quickie sessions could be better for you. In research from Arizona State University published last year, people had consistently lower blood pressure readings on average when they split their daily walk into three 10-minute segments rather than tackling one 30-minute stroll.</p><p  class="MainText">

But while this may be enough to keep up your general health, you'll still need to get more active most days of the week if you're trying to drop some pounds. Matthews' recommendation: Shoot for at least 250 minutes of moderate to vigorous exercise a week for the ultimate slim-down success.</p><p  class="MainText">

Myth: More gym time is better.</p><p  class="MainText">

Fact: "Scheduling in rest days is crucial," stresses Los Angeles-based celebrity trainer Ashley Borden. "Your body needs to recover, especially after a tough session."</p><p  class="MainText">

If you work out every single day, you could injure yourself or overtrain, which keeps your muscles from rebounding and your body from improving. That's true even if you're just a casual gym-goer. So be sure to take regular breaks, whether it's every other day (if you're a beginner) or once a week (for the advanced).</p><p  class="MainText">

And keep your workout varied! "If you don't mix things up," Valerie Waters, personal trainer to Jennifer Garner, warns, "doing the same training pattern can lead to injuries."</p><p  class="MainText">

Surprising fact: Skipping sleep can cause weight gain.</p><p  class="MainText">

Women in an American Journal of Epidemiology study who slept less than seven hours were more likely to gain weight; other research has shown that even partial sleep deprivation ups production of the hormone ghrelin, which triggers hunger.</p><p  class="MainText">

Surprising Fact: Yoga isn't a big calorie burner.</p><p  class="MainText">

While doing yoga does improve flexibility and strength, it's not much of an aerobic activity, according to an ACE study: a 50-minute power yoga session burns 237 calories, versus the 500 to 600 calories you'd fry by Spinning for that amount of time.</p><p  class="MainText">

Surprising Fact: Lifting weights won't bulk you up.</p><p  class="MainText">

Even if you're using heavy dumbbells, you're not going to turn into a female Thor &ndash; really! "Women typically have less muscle tissue and produce lower levels of testosterone than men," says Matthews, meaning we're less physiologically prone to becoming brawny.</p>

