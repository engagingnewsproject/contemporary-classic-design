<p class="MainText">The wait is finally over for aviation fanatics wanting to book a flight aboard the Airbus A350 XWB. Qatar Airways, the global launch customer of the A350 XWB, accepted delivery of the commercial plane during a ceremony at Airbus' headquarters in Toulouse, France, last Monday.</p><p class="MainText">

"It is more than a great day for Airbus," the manufacturer's CEO, Fabrice Bregier, told a press conference. "I would say it was an historic day."</p><p  class="MainText">

Qatar Airways opted for a two-class, 283-seat configuration A350, offering 36 business class seats and 247 in economy class. In total, the airline has ordered 80 of the airplanes. Qatar Airways' new A350 will enter into commercial service next month, operating daily flights between its Hamad International Airport hub in Doha, Qatar, and Frankfurt, Germany.</p>
