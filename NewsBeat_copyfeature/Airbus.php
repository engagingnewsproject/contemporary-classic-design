  <p class="Photocaption">The "XWB" in the name means "extra wide body."</p>
  <hr>
<p class="MainText">The A350 XWB is the first in a family of super-efficient passenger planes Airbus designed to go head-to-head with rival Boeing's 787 Dreamliner and 777s. The project for the new jet began in 2006 and was initially criticized by some in the industry, including airlines, for not really advancing the global aviation scene. Being publicly lambasted by some of its largest customers &ndash; one aviation executive called it a Band-Aid reaction to the 787 Dreamliner, while the CEO of Singapore Airlines said the plane just didn't go far enough &ndash; the pressure was on for Airbus to come up with a game changing plane.</p><p  class="MainText">

Much of the aircraft we see today is the result of that pressure. Made from advanced materials, 53% of the A350 XWB frame consists of carbon-fiber reinforced polymer, including Airbus' first carbon-fiber fuselage. As a result, Airbus says the plane will burn 25% less fuel than existing similar size jets.</p><p  class="MainText">

The A350 XWB is powered by two massive Rolls Royce Trent XWB engines, though those who have experienced its test flights say it's remarkable just how quiet the plane actually is. Airbus says passengers will also get a higher quality of air (replaced on the aircraft every two to three minutes), wider cabins, and 18-inch-wide seats in economy.</p><p  class="MainText">

As of the end of November, Airbus has received 778 firm A350 orders from 41 airlines. To meet this demand, Airbus says it's ramping up production progressively to reach the planned rate of 10 aircraft per month by 2018. The 2014 list price for the aircraft is $295.2 million.</p><p  class="MainText"> 

With the first of nearly 800 A350s set to hit the skies, plane spotters will be particularly keen to see the sporty new jet in flight. So how will you know when you're looking at one?</p><p  class="MainText">

Key distinguishing features include a streamlined nose and wrap-around cockpit windows. But perhaps the most noticeable feature is the plane's tapered wings, which Airbus says makes the plane faster, more efficient and quieter. These include winglet-type devices, known as "sharklets," that reduce aerodynamic drag.</p>
