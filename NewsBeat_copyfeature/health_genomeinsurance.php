<p class="MainText"><br>About 700,000 Americans have had their DNA sequenced, in full or in part, and the number is rising rapidly as costs plummet &ndash; to $1,000 or less for a full genome, down from more than $1 million less than a decade ago.</p><p class="MainText">

But many people are avoiding the tests because of a major omission in the 2008 federal law that bars employers and health insurers from seeking the results of genetic testing.</p><p class="MainText">

The Genetic Information Nondiscrimination Act, known as GINA, does not apply to three types of insurance &ndash; life, disability and long-term care &ndash; that are especially important to people who may have serious inherited diseases. Sponsors of the act say that they were well aware of the omission, but that after a 14-year effort to write and pass the law, they had to settle for what they could get.
</p>

