  <p class="Photocaption">The low-cost availability of genome sequencing has many worried about insurance premiums.</p>
  <hr>
<p class="MainText">That leaves many patients who may be at risk for inherited diseases fearful that a positive result could be used against them.</p><p  class="MainText">

They include Brian S., a 33-year-old surgical resident in Pennsylvania, who has a 50 percent chance of carrying a genetic mutation that causes Cadasil, a fatal neurological disorder that afflicts his mother. "I kind of want to get tested," Dr. S. said, speaking on the condition that his last name and other identifying details be withheld. But because he wants to apply for life and long-term-care insurance, he has decided against it.</p><p  class="MainText">

There is no way of knowing how many people fall into this category, but experts say such concerns are mounting.</p><p  class="MainText">

"It was all moot a few years ago," said Dr. James P. Evans, a professor of genetics at the University of North Carolina, Chapel Hill. "It's suddenly now become real because people increasingly have access to what's in their genomes."</p><p  class="MainText">

Dr. Robert C. Green, a genetics researcher at Harvard Medical School, studied the behavior of those who had recently learned they carried a genetic marker that predisposes them to early Alzheimer's disease. They were five times as likely to buy long-term-care insurance as those in a control group.</p><p  class="MainText">

But while patients seek the protection that insurance offers, many are concerned about the possibility of paying higher premiums or being denied coverage altogether because of the known existence of a dangerous mutation.</p><p  class="MainText">

"The fear is potent in our society that insurance companies are asking," Dr. Green said. "The No. 1, 2 and 3 issue that subjects are concerned about is, will they be discriminated against if this is in their medical record?"</p><p  class="MainText">

Just three states &ndash; California, Oregon and Vermont &ndash; have broad regulations prohibiting the use of genetic information in life, long-term-care and disability insurance.</p><p  class="MainText">

At least one insurer, the Northwestern Mutual Life Insurance Company, asks potential customers in Massachusetts about genetic testing â€” and stipulates that refusing to share results could lead to a declined application or an extra premium. Jean Towell, a spokeswoman, says applicants are told "out of fairness" that insurers have the right to decline coverage if any medical information is omitted.</p><p  class="MainText">

"We think it's best to have it all spelled out in black and white so buyers can make a well-informed decision," she said.</p><p  class="MainText">

At least for now, 12 other companies ask no explicit questions about genetic testing. But when Dr. Green asked company executives why not, he said, "at least one of them has told me, 'We would do this, but we don't want to be the first.' "</p><p  class="MainText">

Still, he added, "you can imagine a world where millions of people have this information, and that would reach a tipping point that the insurance companies can no longer ignore."</p><p  class="MainText">

Even if most insurers are not asking now, they do seek out medical records and can use genetic test results listed there. By contrast, under the federal law, an employer who asks for an employee's records must tell the provider to withhold any genetic information.</p><p  class="MainText">

Robin Bennett, a genetics counselor at the University of Washington, sees patients almost every day who express fears about how their test results might be used.</p><p  class="MainText">

Some ask, "could we not put it in their medical records," she said, "but they don't realize that if we're going to take action on their information" &ndash; such as preventive surgery to lower risk for breast cancer &ndash; "it has to be in their records."</p><p  class="MainText">

The American Medical Association's code of ethics states that "it may be necessary" for doctors to maintain a separate file for genetic test results so the information is not sent to insurers. A study published last year by Dr. Robert L. Klitzman, who directs the Masters of Bioethics Program at Columbia University (in which this reporter is a student), found that 4.5 percent of 220 general internists admitted to hiding or disguising genetic information.</p><p  class="MainText">

"With electronic medical records, it's not clear what will happen," he said. "Will it become impossible to hide certain kinds of data? Will there be a way to section some things off? A lot of this is still being worked out."</p><p  class="MainText">

But even if such results can be kept private, patients could be penalized. A life insurance broker for Accuquote, an online service that compares insurance policies, said that if an applicant carried a highly predictive marker for a disease like Alzheimer's and failed to disclose it, that would be "guilt by omission."</p><p  class="MainText">

As the cost of sequencing falls, some experts worry that insurers may run their own tests on specimens they collect. Only one state, New Mexico, requires that applicants be informed of such tests.</p><p  class="MainText">

Fear of discrimination affects research participants as well as patients. Dr. Green said that 23 out of 94 volunteers who walked away from a sequencing study did so because of concerns about insurance.</p><p  class="MainText">

To recruit subjects who refuse genetic testing, Dr. Steven M. Hersch, a laboratory director at the MassGeneral Institute for Neurodegenerative Disease, designed a study to allow patients at risk for Huntington's disease to participate in a clinical trial without being told their mutation status.</p><p  class="MainText">

"Insurance fears play a big role," he said. These worries, he added, are spreading to a growing community of people aware of predictive testing for hereditary illnesses like Alzheimer's, breast cancer and colon cancer.</p><p  class="MainText">

Not everyone is convinced that such discrimination is a major problem.</p><p  class="MainText">

"There's this theoretical risk," said Dr. Leslie G. Biesecker, a chief investigator at the National Human Genome Research Institute, adding that he was not aware of anyone who had faced discrimination after participating in genetic research.</p><p  class="MainText">

"Participants are more likely to be in serious car crash going to or from the research center," he said.</p><p  class="MainText">

But Dr. Klitzman, at Columbia, says genetic discrimination can be subtle.</p><p  class="MainText">

"Someone may not know exactly why they were turned down," he said. Or patients who have been discriminated against "may not go public because then they'd be letting everyone know they have X mutation."</p><p  class="MainText">

Consumer advocates say comprehensive federal legislation is needed to assure people they can safely participate in genetic research and testing.</p><p  class="MainText">

"We are already late in the process of addressing this issue through public policy and law," said Jeremy Gruber, president of the Council for Responsible Genetics. "The question is how soon are we going to realize that we need to act now."</p><p  class="MainText">

Insurance industry representatives, by contrast, argue that they may need genetic information to make underwriting decisions.</p><p  class="MainText">

Barring long-term-care and life insurers from obtaining applicants' test results "could lead to adverse selection and impact the stability of rates," the American Academy of Actuaries said in a statement.</p><p  class="MainText">

The author of the 2008 law, Representative Louise M. Slaughter, a Democrat from upstate New York, was noncommittal about any changes. "Given the rapid pace of technological innovation, the uncharted waters that we are navigating when it comes to genetic information, and the arduous, 14-year effort to finally pass GINA into law, we must be extremely judicious in our next steps," she said.</p><p  class="MainText">

As for Brian S., he and his wife want to have a baby. But because he has decided not to be tested for the fatal neurological disorder, they are considering in vitro fertilization with pre-implantation genetic diagnosis.</p><p  class="MainText">

That would allow any embryos carrying the dreaded mutation to be silently screened out, keeping him ignorant of his own status &ndash; and able to apply for life and long-term-care insurance without fear of being turned down.
</p>

