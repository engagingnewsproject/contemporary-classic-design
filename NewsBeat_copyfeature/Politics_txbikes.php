<p class="MainText">But these days, bicycles are becoming a bigger conversation across the state, as traffic snarls grow worse. The traffic in Dallas, Austin and Houston routinely makes top 10 lists when it comes to most congested U.S. cities. So besides Austin, the state's other major cities, San Antonio, Dallas and Houston &ndash; where oil and gas companies dominate the economy &ndash; are leading several pro-cycling policy changes.</p><p  class="MainText">

"At the city level, it's absolutely amazing. It's on fire," says Robin Stallings, who heads , the statewide advocacy group that pushes for bike-friendly policies. "Not everybody has room for a truck if everybody's in a truck. So we need to at least get some people out of their trucks to make room for the rest of us."</p><p  class="MainText">

To get there, Houston approved for bike trails. San Antonio plans to . Dallas unveiled plans to lay out a new network of 1,100 miles of bike lanes over the next decade. All of this is rooted in a very Texas kind of reason: City leaders realize bike lanes are good for business.</p><p  class="MainText">

"Companies like Samsung and Google are looking at the bicycle facility infrastructure before they decide what city they're going to locate in. So this is really being driven by economics in Texas. It's not all about people seeing themselves on a bicycle, but seeing what it does for the quality of life in a city," Stallings says.</p><p  class="MainText">

Last December, the capital city to start a bike-sharing program. Anyone with at least $8 to spend can rent a bike and ride it around and drop it off, at any of Austin's 40 bike-share sites.</p><p  class="MainText">

"It's nice when I don't know where I'm gonna be meeting people or I'm going out for happy hour and I want to hop a ride to the next spot," says Austin cyclist Shawn Ironmonger.</p><p  class="MainText">

But for cycling culture to catch up to car culture, a long road lies ahead. in Dallas, only 1 in 1,000 people cycles for transportation. The average for the country's largest cities is 16 times higher. And some of the state's biggest barriers to cycling are practical, not political. Commutes are long, and summer temperatures fall somewhere between hot and hellish.</p><p  class="MainText">

"Once it gets to where I need a real good shower once I get somewhere, I can't really do it, for commuting," Ironmonger says.</p><p  class="MainText">

When it comes to attitudes about a different form of transportation, pedicabbie Matherne says the car-only cohort still isn't so willing to share the roads.</p><p  class="MainText">

"I wish there was a way to change people's view of that. Maybe, maybe it will slowly start happening," he says.
