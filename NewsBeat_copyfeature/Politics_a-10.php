  <p class="Photocaption">It's often called the military's ugliest aircraft, a snub-nosed tank of an airplane that's nicknamed "Warthog" for its appearance and ferocity. The A-10 Thunderbolt has been the Air Force's equivalent of an in-the-trenches grunt for almost 40 years: heavily armed and armored, designed to fly low and take out the enemy at close range.</p>
  <hr>
<p class="MainText">Supporters of the A-10 have launched an aggressive campaign to save an aircraft they say is unparalleled in the history of American aviation: a slow-flying airplane designed to fly close enough to the ground so that pilots can distinguish friend from foe, often with their own eyes.</p>
<p  class="MainText">

The A-10 has saved dozens of lives in Iraq and Afghanistan. And it has performed in a way that modern planes &ndash; flying high and fast &ndash; never could, they say. </p><p  class="MainText">

"The best close air support platform we have around is the A-10," U.S. Sen. Kelly Ayotte (R-N.H.) said at a press conference, where she was joined by Sen.John McCain (R-Ariz.) and several A-10 pilots. "And we owe it to our men and women in uniform to ensure that they have the best when it comes to this incredibly important mission".</p><p  class="MainText">


It is beloved not just by pilots, but by the ground troops under fire who equate the high-pitched whine of the A-10 and the roar of its cannon with salvation. In recent Congressional hearings it has gotten rave reviews, particularly by the Army brass.</p><p  class="MainText">

"The A-10 is the ugliest most beautiful aircraft on the planet," said Army Gen. Martin Dempsey, the chairman of the Joint Chiefs of Staff. </p><p  class="MainText">
 
 "It's a game changer," said Army Vice Chief of Staff Gen. John Campbell. "It's ugly. It's loud but when it comes in and you hear that pffffff [of the cannon] it just makes a difference."</p><p  class="MainText">
 Air Force officials argue that with the defense spending cuts they have no choice but to get rid of the entire A-10 fleet. Welsh, the Air Force chief of staff, told the Senate Armed Services Committee Thursday that officials had looked at ways to save the A-10 by cutting other programs. But they ultimately decided that the A-10 was the option with the lowest risk.
 
"The budget picture we're presenting to you today is hard choices, nothing but hard choices," Air Force Secretary Deborah Lee James told the committee. She called the A-10 a "wonderful aircraft. But there are other aircraft that can cover that very sacred combat air support mission." </p><p  class="MainText">
 
Welsh said that the Air Force must be prepared for "a full sprectrum fight" that involves many missions in addition to close air support.  </p><p  class="MainText">
 
 "We have a lot of other airplanes that do close air support that can do those other important things," he said. "The A-10 isn't used in that way. It doesn't mean it's not a great platform. ... The comment I've heard that somehow the Air Force is walking away from close air support I must admit frustrates me." </p><p  class="MainText">
 
 One of those aircraft will be the F-35 Joint Strike Fighter, officials have said. But the aircraft, which has been repeatedly delayed and has seen its cost skyrocket, is not expected to be ready until at least 2021. And the Air Force is planning to get rid of the A-10s by 2019.</p><p  class="MainText">
  
  Ayotte, who inserted language in the defense spending bill that prevents the Air Force from retiring the A-10 before the end of 2014, called that a dangerous gap.</p><p  class="MainText">
  
McCain was more blunt. </p><p  class="MainText">
 
"We are going to do away with the finest close air support weapon in history?" he said at the news conference. "And we are then going to have some kind of nebulous idea of a replacement with an airplane that costs at least 10 times as much &ndash; and the cost is still growing &ndash; with the F-35? That's ridiculous. That's absolutely ridiculous." </p><p  class="MainText"> 

Supporters also pointed to how this is the Air Force's latest attempt to get rid of the A-10 so that it could focus on more advanced aircraft.</p><p  class="MainText"> 
  
A 1988 report from Congress's nonpartisan investigative arm, now known as the Government Accountability Office, said it had been tasked to look into the viability of the A-10 because "the Air Force is concerned about the A-10's ability to support the Army and survive the Soviet air defense threat of the 1990s and beyond." </p>
