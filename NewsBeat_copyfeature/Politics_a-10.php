But now, after a career that has spanned the Cold War to the conflicts in Iraq and Afghanistan, the Pentagon has proposed shuttering the fleet as part of across the board cuts in defense spending. Getting rid of the remaining about 300 aircraft would save $3.7 billion over five years, Defense Department officials say, and allow the Air Force to bring in more sophisticated aircraft, such as the F-35 Joint Strike Fighter, to provide what is called "close air support."</p><p  class="MainText">

While no one, especially me, is happy about recommending divestiture of this great old friend, it's the right military decision," Air Force Chief of Staff Gen. Mark Welsh told the Senate Armed Services Committee. "And it's representative of the extremely difficult choices that we're being forced to make."</p>
