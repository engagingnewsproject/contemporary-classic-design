  <hr>
<p class="MainText">The Keys cut Turn Blue in L.A., Michigan and Auerbach's studio in Nashville, alongside co-producer Brian "Danger Mouse" Burton, who first worked with the band on 2008's Attack & Release. Whereas the Keys' last record, 2011's El Camino, was polished and hooky, Turn Blue has a groove-driven psychedelic glaze. "Fever" is a dance-floor throwdown with a Farfisa organ hook, and on the title track, Auerbach uses his falsetto over ambient synths and crisp jazz chords as he repeats, "I really don't think you know/There could be hell below." "We were sort of making a headphone record," says Auerbach. Adds drummer Patrick Carney, "It pays off to listen more than once."</p><p class="MainText">

The closing track stands out as the most immediately catchy: "Gotta Get Away," a Creedence-style rave-up about traveling across the country to escape a tumultuous relationship. The Keys wrote it in 10 minutes. "It made me a little bit uncomfortable because it was so classic-sounding, but we couldn't deny it," says Auerbach. "At this point, there are absolutely no rules."</p>

