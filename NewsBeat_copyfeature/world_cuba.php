The Cuban parliament will vote on the proposed new foreign investment law Saturday. It would grant eight-year tax exemptions for new foreign investments, cut profit taxes in half, and allow foreign money in all sectors except education, health and the military.</p><p  class="MainText">

While few details of the bill have been disclosed, state-owned media is reporting that the new law will give investors legal protection.</p><p  class="MainText">

Cuba has allowed foreign investment since 1995, but cumbersome red tape and the lack of legal protections have sent foreign investors running. Over the last decade, the number of foreign ventures in Cuba declined by half.</p><p  class="MainText">

Currently, foreign investors complain they have little legal recourse when disputes with the government or state partners arise.</p><p  class="MainText">

The new law aims to address those issues and will also allow 100 percent foreign-owned companies to operate in Cuba, although it says those firms will not be able to claim any tax shelters.</p><p  class="MainText">

The National Assembly is expected to approve the draft with little change.</p>

