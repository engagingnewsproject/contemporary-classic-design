  <p class="Photocaption">Pictured here is the multimillion dollar facility created by the University of Oregon for its athletic programs. An upcoming report by the AAUP highlights the contrast between athletic and academic spending by universities.</p>

  <hr>

<p class="MainText">

"Many of us have had the concern that the out-of-control expenditures in Division I would have a cascading effect, and this report suggests that our worst fears are coming to pass," said William Kirwan, co-chairman of the Knight Commission and chancellor of the University System of Maryland. "The American culture is so in love with athletics that even though many people know the right thing to do, they can't do it."</p><p  class="MainText">

Using data from the United States Department of Education, the N.C.A.A., and its own surveys, the association paints a sobering picture in its report, titled "Losing Focus," of a sector in which the growth in educational spending trails far behind that of athletic spending &mdash; especially at community colleges and Division II and III institutions.</p><p  class="MainText">

From 2004 to 2011, community colleges' inflation-adjusted educational spending &mdash; on instruction, public service and academic support &mdash; declined, while their athletic spending increased 35 percent per athlete, the report said. Overall spending per student grew 2.6 percent.</p><p  class="MainText">

Inflation-adjusted athletic spending also increased, by 24.8 percent, at public four-year colleges in all divisions in those years, while spending on instruction and academic support remained nearly flat, and public service and research expenditures declined, the report said. Their overall spending per student grew 1.6 percent.</p><p  class="MainText">

The fastest growth in athletic spending was at Division III schools without football programs, where median inflation-adjusted spending for each student-athlete more than doubled from 2004 to 2012.</p><p  class="MainText">

"Something has gone awry, something's mismatched, between the mission of colleges and universities and their athletic spending," said John Curtis, director of research for the American Association of University Professors and a co-author of the report.</p><p  class="MainText">

A spokeswoman for Mark Emmert, the president of the National Collegiate Athletic Association, said he would not be available for comment on the report.</p><p  class="MainText">

Terry W. Hartle, senior vice president of the American Council on Education, which represents the leaders of colleges and universities, said that the numbers in the report, viewed in context, may not be alarming.</p><p  class="MainText">

"This comes from the American Association of University Professors, which has a vested interest in finding that too little money is going to faculty and too much to sports and administration," Mr. Hartle said. "If you just look at the percentage increases, without the base they're working from, it's hard to tell what it means."</p><p  class="MainText">

Saranna Thornton, the other co-author of the university professors report, said athletic spending by the Division II and III schools and community colleges has attracted little notice because the teams are not a television staple.</p><p  class="MainText">

"We all see college football, we know the shiny helmets at the University of Tennessee. We know March madness, but we don't see Division III games, or the community colleges," said Ms. Thornton, an economics professor and coach of men's rugby at Hampden-Sydney College in Virginia. Even colleges without powerhouse sports programs, she said, are racing to build their athletic programs as a recruitment tool.</p><p  class="MainText">

"My hypothesis, and it's not yet fully proven, is that these are mostly schools that are very tuition-dependent, and they're spending more on sports to recruit more students," Ms. Thornton said. "But I think it's ludicrous."</p><p  class="MainText">

The report also said that the ranks of administrators and adjunct professors are growing many times faster than full-time tenured, or tenure-track faculty. Faculty salaries increased 2.4 percent last year, on average, while top administrators received large raises.</p>

