<table bgcolor="#FFFFFF"; style="text-wrap:normal; word-wrap:normal; max-width:730px" cellpadding="10px"><tr><td align="left"><p class="MainText">Even as their spending on instruction, research and public service declined or stayed flat, most colleges and universities rapidly increased their spending on sports, according to a report being released Monday by the American Association of University Professors.</p><p class="MainText">

"Increasingly, institutions of higher education have lost their focus on the academic activities at the core of their mission," the association said in its report. "The spending priority accorded to competitive athletics too easily diverts the focus of our institutions from teaching and learning to scandal and excess."</p><p class="MainText">

For years, the Knight Commission on Intercollegiate Athletics, a watchdog group of academic leaders and others, has been documenting, and deploring, the race in sports expenditures at the most competitive level, Division I. But this report is believed to be the first that also compares educational spending and athletic spending, over time, at Division II and III schools and at community colleges.</p>

