  <strong>Colleges Increasing Spending on Sports Faster Than on Academics, Report Finds</strong></span>

<p class="Photocaption" align="left">BY TAMAR LEWIN</p>

<p ><img src="Print.jpg" alt="" width="181" height="31" /></p>

